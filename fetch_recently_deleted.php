<?php
// fetch_recently_deleted.php
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "payroll";

// Create connection
$conn = new mysqli($servername, $username, $password, $dbname);


$sql = "SELECT * FROM employees WHERE deleted_at IS NOT NULL";
$result = $conn->query($sql);

